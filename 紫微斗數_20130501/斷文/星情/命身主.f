: 命主論斷 ( -- ) 
 s" 命主乃先天俱有的特性，是指先天的格局與生俱有非自己今生努力的結果，乃先天魂所居之所，佛家曰人假四大地水火風而成形，此為之先天也。" 斷文
 s" 喜入於命、財、帛、官、祿、田、宅、子女、夫妻、福德宮與主星廟旺與長生帝旺等旺宮較好。" 斷文 
 s" 如果在友、疾、遷、父宮則次之，加煞星則更不好。" 斷文
 s" 命主是用來輔助命宮的，代表環境對人類命運的影響。" 斷文
 s" 命主所到的宮位，會因這個宮吉而成，也會因這所代表的意義也就有所不同，在分析判斷時，先看命主是何星曜，次看落入何宮稱，再看逢吉或逢凶，最後才來論斷是好是壞。" 斷文 
 s" 命主逢吉的人，可以在人生生涯中，盡量的去做最利於自己的選擇。" 斷文
 s" 而命主落位差又逢凶的人，也不必唉聲嘆氣，只要懂得兇在何處，問題出現在那裡，就可以趨吉避凶，這一生中也就沒有什麼悔吝了！" 斷文 
 s" 命主星是命宮的守護神，主宰人的富貴貧賤禍福壽夭，與精神思想有關，是先天具有的特性。在具體的運用中，命主星的好壞僅作為判斷命宮吉凶的補充。" 斷文
 s" 命主星宜廟旺、在生旺之宮、與吉星同宮則為吉論；若失陷、在死絕空亡之宮、與煞星同宮則為凶論。" 斷文
 s" 命主星廟旺、在生旺之宮、得宮生，可加強命宮的力量，再得吉星吉格，必可增強富貴福壽的力度；若逢失陷、在死絕之宮、受宮克，可減弱命宮的力量，再逢凶星兇格，則增強貧賤夭殘的力度。" 斷文
