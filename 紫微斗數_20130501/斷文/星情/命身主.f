: 命身主論斷 ( -- )
 s" 初習鬥術者，只要對星曜性質有所了解即可論人形性．是以對命身主因不解或困惑而忽略，其實命身主在斗術上佔有相當地位，就個人習得，探討如下，以供參考．" 斷文
 s" 一般說法：命身主乃統馭命身宮三方四正星曜之形性與形貌，但因顯像不明顯，而被忽略．" 斷文
 s" 命身主既是統馭命身宮三方之星曜，則陽宅風水之擇定，或許可以命身主為依據，尤以命無主星或雙星者，僅供參考．" 斷文
 s" 但若命身主出現在命身宮三方四正宮位裡，命身主常常成顯性作用．尤以命宮無主星或命身宮星曜閒弱時，一般看命宮強弱，除少數例外．" 斷文
 s" 是以星曜之廟旺利陷及長生十二神論，其實命身主座宮及所同星曜亦是參考因素之一．" 斷文
 s" 是以書雲二主逢劫空，衣食不足，謀為不遂．命身主是否被衝破，乃是判斷災厄輕重，是否死亡之因素之一．" 斷文
 s" 命主身主乃源於星宗法。西洋占星術有所謂的守護星: 比如天蠍座卯宮，守護星是火星、冥王星。" 斷文
 s" 而星宗以卯戌合火，以火為宮主，西洋則稱宮主星。" 斷文
 s" 鬥數就是承接了星宗的體系，故有命主、身主、宮主(夫妻主、兄弟主等等)。" 斷文
 s" 論諸星分屬南北斗化吉凶並分屬五行，紫微屬土中天星，南北斗化帝座為官祿主。天機星屬木男北斗化善為兄弟主。" 斷文
 s" 事實上十四主星都有所化，配合化、主、及星的陰陽五行就能抓住主星的賦性。" 斷文
